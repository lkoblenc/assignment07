			Daenerys Targaryen is the daughter of King Aerys II Targaryen (also referred to as "The Mad King") and his sister-wife Queen Rhaella, and is one of the last survivors of House Targaryen. She serves as the third-person narrator of 31 chapters throughout A Game of Thrones, A Clash of Kings, A Storm of Swords, and A Dance with Dragons. She is the only monarch or claimant to a throne given point of view chapters in the novels. Thirteen years before the events of the series (16 in the television series), after her father and eldest brother Rhaegar were killed during Robert's Rebellion, Daenerys was born in the midst of a great storm, earning her the nickname "Stormborn". Rhaella died in childbirth, and Daenerys and her older brother Viserys were whisked away to Braavos by the Master of Arms of the Red Keep, Ser Willem Darry. Darry died when Daenerys was 5, and she and Viserys spent the following years wandering the Free Cities. By the beginning of A Game of Thrones, Daenerys has been a guest of Illyrio Mopatis' in Pentos for half a year. Game of Thrones creators D. B. Weiss and David Benioff described Daenerys as a combination of Joan of Arc, Lawrence of Arabia, and Napoleon.
